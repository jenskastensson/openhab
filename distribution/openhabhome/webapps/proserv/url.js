document.addEventListener('click', function (event) {
  var target = event.target;
  if (target.tagName.toLowerCase() == 'a') {
    var port = target.getAttribute('href').match(/^:(\d+)(.*)/);
    if (port) {
      target.href = port[2];
      target.port = port[1];
      if (target.port == 8443)
        target.protocol = 'https:';
    }
  }
}, false);